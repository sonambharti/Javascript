// Single line Comments
/*
Multi-line comments
*/  
let a = 4;
let b = 5;
console.log("a + b = ", a + b);

console.log("a - b = ", a - b);

console.log("a * b = ", a * b);

console.log("b / a = ", b / a);

console.log("a % b = ", a % b); // Modulus Operator

console.log("b ^ a = ", b ** a); // Exponentiation Operator

// Unary Operator
let c = 10;
console.log("c++ = ", c++); // Post-Increment Operator
console.log("++c = ", ++c); // Pre-Increment Operator


console.log("c-- = ", c--); // Post-Decrement Operator
console.log("--c = ", --c); // Pre-Decrement Operator

// Logical Operators
let l = 0;      
let m = 10;
console.log("l && m = ", l && m); // Logical AND Operator
console.log("l || m = ", l || m); // Logical OR Operator
console.log("!l = ", !l); // Logical NOT Operator
console.log("l == m = ", l == m); // Equality Operator
console.log("l != m = ", l != m); // Inequality Operator


// Bitwise Operator
let bi = 5;
console.log("bi & 3 = ", bi & 3); // Bitwise AND Operator
console.log("bi | 3 = ", bi | 3); // Bitwise OR Operator
console.log("bi ^ 3 = ", bi ^ 3); // Bitwise XOR Operator
console.log("~bi = ", ~bi); // Bitwise NOT Operator
console.log("bi << 1 = ", bi << 1); // Left Shift Operator 
console.log("bi >> 1 = ", bi >> 1); // Right Shift Operator 
console.log("bi >>> 1 = ", bi >>> 1); // Zero Fill Right Shift Operator

// Comparison Operator
let x = 5;  
let y = 10;
console.log("x == y = ", x == y); // Equal To Operator
console.log("x != y = ", x != y); // Not Equal To Operator
console.log("x > y = ", x > y); // Greater Than Operator
console.log("x < y = ", x < y); // Less Than Operator
console.log("x >= y = ", x >= y); // Greater Than or Equal To Operator
console.log("x <= y = ", x <= y); // Less Than or Equal To Operator
console.log("x === y = ", x === y); // Strict Equal To Operator
console.log("x !== y = ", x !== y); // Strict Not Equal To Operator


// Asignment Operator
let d = 10;
console.log("d += 5 = ", d += 5); // Addition Assignment Operator
console.log("d -= 5 = ", d -= 5); // Subtraction Assignment Operator
console.log("d *= 5 = ", d *= 5); // Multiplication Assignment Operator
console.log("d /= 5 = ", d /= 5); // Division Assignment Operator
console.log("d %= 5 = ", d %= 5); // Modulus Assignment Operator
console.log("d **= 5 = ", d **= 5); // Exponentiation
console.log("d <<= 5 = ", d <<= 5); // Left Shift Assignment Operator
console.log("d >>= 5 = ", d >>= 5); // Right Shift Assignment Operator
console.log("d >>>= 5 = ", d >>>= 5); // Unsigned Right Shift
console.log("d &= 5 = ", d &= 5); // Bitwise AND Assignment Operator
console.log("d ^= 5 = ", d ^= 5); // Bitwise XOR Assignment Operator
console.log("d |= 5 = ", d |= 5); // Bitwise OR Assignment Operator

// conditional Operator
let e = 10;
console.log("e > 5 ? e : 5 = ", e > 5 ? e : 5); // Conditional Operator

// Template literals
let name = "John";      
let age = 25;
console.log(`My name is ${name} and I am ${age} years old.`); 
console.log(`My name is ${name} and I am ${age} years old. \nI am ${age > 18 ? "adult" : "minor"}.`);
console.log(`My name is Sonam\tBharti`);


